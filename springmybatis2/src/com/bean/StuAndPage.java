package com.bean;

public class StuAndPage {

	//将页码和学生封装成对象
	private Page page;
	private Student stu;
	public Page getPage() {
		return page;
	}
	public void setPage(Page page) {
		this.page = page;
	}
	public Student getStu() {
		return stu;
	}
	public void setStu(Student stu) {
		this.stu = stu;
	}
}
